typedef struct tagSUARTPORT
{
    // Whether the port is currently connected
    BOOL bConnected;

    // Head pointer of the cyclic buffer (used for inserting data to buffer)
    CHAR* pbyHead;

    // Tail pointer of the cyclic buffer (used for outputting data from buffer)
    CHAR* pbyTail;

    // Start address of the cyclic buffer
    CHAR* pbyStart;

    // First address beyond the cyclic buffer
    CHAR* pbyEnd;

    // Cyclic buffer size
    UINT32 ulSize;

    // Current number of bytes in the cyclic buffer
    // (Used only by UartDB.c!).
    UINT32 ulTotal;

    // Pointer to the application buffer (for reading operation)
    CHAR* pbyInBuffer;

    // Number of bytes to be read.
    UINT32 ulToRead;

    // Number of bytes already read.
    UINT32 ulWasRead;

    // Pointer to the application buffer (for write operation)
    CHAR* pbyOutBuffer;

    // Number of bytes to be write.
    UINT32 ulToWrite;

    // Current User ID
    UINT32 uiCurrentUserId;

    // Shadow for UART_ADDR_LINE_CTL.
    UINT16 uwLineCtl;

    // Baud rate used.
    UINT32  ulBaudRate;

    // When TRUE, hardware flow-control is configured for this port.
    BOOL   bHardwareFlowControlEnabled;

    // Input FIFO level.
    // When not using flow-control - level for which an interrupt is raised.
    // When using flow-control - see UartOpenFifo() for details.
    UINT32  ulInFifoLevel;

} SUARTPORT, *PSUARTPORT;

